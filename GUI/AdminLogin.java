    private void BtnLoginActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_BtnLoginActionPerformed
        String AdminUsername = t1.getText().trim(); // tostring and delete Space
        String AdminPassword = new String(p1.getPassword());

        if (AdminUsername.isEmpty() || AdminPassword.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please complete all fields.", "WARNING", JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (AdminPassword.length() < 8) {
            JOptionPane.showMessageDialog(this, "Password must at least 8 characters.", "WARNING",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        // üîπ Path ‡πÑ‡∏ü‡∏•‡πå CSV
        String filePath = FILE_DIR + "/admin_password.csv";

        boolean isLoginSuccess = false;

        try (java.io.BufferedReader br = new java.io.BufferedReader(new java.io.FileReader(filePath))) {
            String line;
            while ((line = br.readLine()) != null) {
                String[] data = line.split(",");
                if (data.length >= 2) {
                    String username = data[0].trim();
                    String password = data[1].trim();

                    // üîπ ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö username ‡πÅ‡∏•‡∏∞ password
                    if (AdminUsername.equals(username) && AdminPassword.equals(password)) {
                        isLoginSuccess = true;
                        break;
                    }
                }
            }
        } catch (java.io.IOException e) {
            JOptionPane.showMessageDialog(this, "Error reading admin file:\n" + e.getMessage(), "ERROR",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (isLoginSuccess) {
            JOptionPane.showMessageDialog(this, "Login Successful!", "SUCCESS", JOptionPane.INFORMATION_MESSAGE);

            Admin adminPage = new Admin(); // <<-- class ‡∏ä‡∏∑‡πà‡∏≠‡∏ß‡πà‡∏≤ Admin ‡πÉ‡∏ä‡πà‡πÑ‡∏´‡∏°
            adminPage.setVisible(true);
            this.dispose(); // ‡∏õ‡∏¥‡∏î‡∏´‡∏ô‡πâ‡∏≤‡∏•‡πá‡∏≠‡∏Å‡∏≠‡∏¥‡∏ô

        } else {
            JOptionPane.showMessageDialog(this, "Invalid username or password.", "ERROR", JOptionPane.ERROR_MESSAGE);
        }

    }// GEN-LAST:event_BtnLoginActionPerformed

